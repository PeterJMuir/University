/*
 * This program assigns the sum of two integer constants to an integer
 * variable, then displays the result along with some annotation.
 */


#include <stdio.h>


int main()
{
   int sum;        /* variable declaration*/
   sum = 10 + 30;  /* assign the value */

   /* display the result */
   printf("The sum of 10 and 30 is %d\n", sum);
   return 0;
}
